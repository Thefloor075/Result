import matplotlib.pyplot as plt
import numpy as np


X = []
Mean = []
Std = []

N = 20

X = list(i for i in range(2,N+1))

N = N - 1

while(len(Mean) != N):
	input_Y = input()
	if input_Y != '':
		Mean.append(float(input_Y))

while(len(Std) != N):
	input_Std = input()
	if input_Std != '':
		Std.append(float(input_Std))


file1 = open("data.txt","w+")
for i in range(N):
	str = '({}, {}) +- (0.0, {})'.format(round(X[i]), round(Mean[i]), Std[i]) + '\n'
	file1.write(str)

file1.close()
	
