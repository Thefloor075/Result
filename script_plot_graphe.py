import matplotlib.pyplot as plt
import numpy as np


X = []
Y = []

print('Entrer la taille du r√©seau')
n = int(input())

while(len(X) != 10):
	input_X = input()
	if input_X != '':
		X.append(float(input_X))

while(len(Y) != 10):
	input_Y = input()
	if input_Y != '':
		Y.append(float(input_Y))


plt.ylabel('Moyenne')
plt.xlabel('%')
plt.plot(100*np.array(X),np.array(Y),'ro')
plt.savefig('Picture_{}.png'.format(n))
plt.figure()
plt.ylabel('Moyenne')
plt.xlabel('%')
plt.plot(100*np.array(X[1::]),np.array(Y[1::]),'ro')
plt.savefig('Picture_{}_1.png'.format(n))

























































































































































































































































































































































































































































































